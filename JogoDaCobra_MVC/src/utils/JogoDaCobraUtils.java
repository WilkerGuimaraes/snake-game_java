package utils;

import java.util.ArrayList;
import java.util.Random;

public class JogoDaCobraUtils {
	public class Bloco {
		int x;
		int y;
		
		public Bloco(int x, int y) {
			this.x = x;
			this.y = y;
		}

		public int getX() {
			return x;
		}

		public void setX(int x) {
			this.x = x;
		}

		public int getY() {
			return y;
		}

		public void setY(int y) {
			this.y = y;
		}
	}
}
